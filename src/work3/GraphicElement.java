package work3;

/**
 * The {@link GraphicElement} interface defines the basic structure for any graphical element
 * that can be manipulated and drawn in the graphic editor.
 * @author Oleksandr "Pliffdax" Stepanov
 */
public interface GraphicElement {
    /**
     * Draws the graphic element. This method must be implemented by any class
     * that represents a graphical element.
     */
    void draw();
}
